package de.mm.spaceinvaders.logic;

public class Entity
{

	private int x, y, health;

	public int getX()
	{
		return x;
	}

	public int getY()
	{
		return y;
	}

	public int getHealth()
	{
		return health;
	}

	public void setX(int x)
	{
		this.x = x;
	}

	public void setY(int y)
	{
		this.y = y;
	}

	public void setHealth(int health)
	{
		this.health = health;
	}

}
